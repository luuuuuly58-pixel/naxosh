/* گۆڕینی ژمارە ئینگلیزییەکان بۆ ژمارەی کوردی/عەرەبی */
function toKurdishDigits(input) {
  const map = ["٠", "١", "٢", "٣", "٤", "٥", "٦", "٧", "٨", "٩"];
  return String(input).replace(/[0-9]/g, d => map[d]);
}

/* دروستکردنی نرخ بە شێوازی خوێندنەوەی ئاسان: ٢٥٬٠٠٠ */
function formatPrice(n) {
  const withSep = String(n).replace(/\B(?=(\d{3})+(?!\d))/g, "٬");
  return toKurdishDigits(withSep);
}
