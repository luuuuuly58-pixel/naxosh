/* گۆڕینی ژمارە ئینگلیزییەکان بۆ ژمارەی کوردی/عەرەبی */
function toKurdishDigits(input) {
  const map = ["٠", "١", "٢", "٣", "٤", "٥", "٦", "٧", "٨", "٩"];
  return String(input).replace(/[0-9]/g, d => map[d]);
}

/* دروستکردنی نرخ بە شێوازی خوێندنەوەی ئاسان: ٢٥٬٠٠٠ */
function formatPrice(n) {
  const withSep = String(n).replace(/\B(?=(\d{3})+(?!\d))/g, "٬");
  return toKurdishDigits(withSep);
}

/* ============================================================
   ژمارەی تەلەفۆن — نۆرماڵکردن بۆ شێوازێکی یەکگرتوو
   هەر سێ شێوازەکە: 07XX XXX XXXX / 7XX XXX XXXX / +964 7XX XXX XXXX
   هەموویان دەگەڕێنەوە بۆ یەک کلیلی نهێنی: 7XXXXXXXXX (١٠ ژمارە).
   بۆیە ئەگەر نەخۆش جارێکی تر بە شێوازێکی جیاواز بینووسێتەوە، سیستەم
   دەزانێت هەمان ژمارەیە و داتاکەی دەگەڕێنێتەوە.
   ============================================================ */
function normalizePhone(raw) {
  const fa = { "٠":"0","١":"1","٢":"2","٣":"3","٤":"4","٥":"5","٦":"6","٧":"7","٨":"8","٩":"9",
               "۰":"0","۱":"1","۲":"2","۳":"3","۴":"4","۵":"5","۶":"6","۷":"7","۸":"8","۹":"9" };
  let s = String(raw || "").replace(/[٠-٩۰-۹]/g, d => fa[d] || d);
  s = s.replace(/\D/g, "");          // تەنها ژمارەکان (بۆشایی، +، - لادەبرێن)
  s = s.replace(/^0+/, "");          // هەر سفرێکی پێشەوە لاببە (07… / 0964… / 00964…)
  if (s.startsWith("964")) s = s.slice(3).replace(/^0+/, "");  // کۆدی وڵات لاببە
  return s;                           // پێشبینی: 7XXXXXXXXX
}
function validPhone(raw) {
  return /^7\d{9}$/.test(normalizePhone(raw));
}
